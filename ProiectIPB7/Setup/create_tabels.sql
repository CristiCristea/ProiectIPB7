   ID_COMISIE  INTEGER,
	 NOTA_1_ORAL  INTEGER,
	 NOTA_1_PROIECT INTEGER,
	 NOTA_2_ORAL INTEGER,
	 NOTA_2_PROIECT INTEGER,
	 NOTA_3_ORAL INTEGER,
	 NOTA_3_PROIECT INTEGER,
	 NOTA_4_ORAL INTEGER,
	 NOTA_4_PROIECT INTEGER,
	 NOTA_FINALA_ORAL INTEGER,
   NOTA_FINALA_PROIECT INTEGER,
	 NOTA_FINALA INTEGER,
	 DATA_SUSTINERE DATE,
	 INTERVAL_ORAR VARCHAR2(20)
);

DROP TABLE VERIFICARE;
CREATE TABLE VERIFICARE(
  COD_VERIFICARE VARCHAR2(20),
